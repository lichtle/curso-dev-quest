/* while (condição) {
    declaração
} */

let a = 10;
let b = 15;

while (a < b) {
  console.log("a vale " + a + " e é menor que b (15)");
  a++;
}

/* do{
  declaração
  } while(condição) */

var i = 1;

do {
  console.log("i vale " + i + " e é menor que 5");
  i++;
} while (i < 5);

for (i = 1; i < 5; i++) {
  console.log("i vale " + i + " e é menor que 5");
}


