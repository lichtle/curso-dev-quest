// SOMA (+)

let primeiroValor = 4;
let segundoValor = 2;

console.log(primeiroValor + segundoValor); // O resultado será o número 6

// Por conta da tipagem fraca do JS, é possível somar variáveis de tipos diferentes, por exemplo números com strings:

let number = 5;
let string = "6";

console.log(number + string); // O resultado será a string "56"

// SUBTRAÇÃO (-)

console.log(primeiroValor - segundoValor); // O resultado será o número 2

// DIVISÃO (/)

console.log(primeiroValor / segundoValor); // O resultado será o número 2

// MULTIPLICAÇÃO (*)

console.log(primeiroValor * segundoValor); // O resultado será o número 8

// RESTO DE UMA DIVISÃO (%)

console.log(primeiroValor % segundoValor); // O resultado será o número 0
